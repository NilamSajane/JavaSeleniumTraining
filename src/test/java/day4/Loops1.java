package day4;

public class Loops1 {

    public static void main(String[] args) {



        /**
         * for (initialization condition; testing condition;
         *                               increment/decrement)
         *{
         *    statement(s)
         *}
         */



         for (int i=1;i<=10;i++)
         {
             System.out.println(i);
         }


    }
}
