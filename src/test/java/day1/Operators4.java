package day1;

public class Operators4 {

    public static void main(String[] args) {

        int i = 10;
        int y = 20;
        /**
         * && --> and
         * || --> or
         * != --> not equal
         */
        System.out.println(i<y || y>i);
        System.out.println(i<y && y>i);
        System.out.println(10 != 11);
    }
}
