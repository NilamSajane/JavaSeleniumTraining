package day1;

public class Operator3 {

    public static void main(String[] args) {

        int i = 10;
        int y = 20;

        System.out.println(i<y);//less than
        System.out.println(i>y);//greater than
        System.out.println(i>=y);// greater than or equal to
        System.out.println(i<=y);// less than or equal to
        System.out.println(i==y);//equals
    }
}
